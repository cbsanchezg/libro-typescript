let día:number=3;
switch(dia){
    case 1: console.log('Lunes'); 
    case 2: console.log('Martes'); 
    case 3: console.log('Miércoles'); 
    case 4: console.log('Jueves'); 
    case 5: console.log('Viernes'); break;
    case 6: console.log('Sabado'); break;
    case 7: console.log('Domingo'); break;
    default: console.log('Número Equivocado');
}
// "Miércoles"
// "Jueves"
// "Viernes"